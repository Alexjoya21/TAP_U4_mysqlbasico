    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name=jTextField1.getText(),
                dom=jTextField2.getText(),
                tel=jTextField3.getText();
        
        p= new Persona(1,name,dom,tel);
        boolean respuesta=base.insertar(p);
        if(respuesta){
            setTitle("Se inserto!!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            cargarTabla();
        }else{
            JOptionPane.showMessageDialog(this,"nemod pa");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        renglonSeleccionado=jTable1.getSelectedRow();
        
        if(evt.getClickCount()>=2){
            Persona pe = new Persona(
                    Integer.parseInt(m.getValueAt(renglonSeleccionado, 0).toString()),
                    m.getValueAt(renglonSeleccionado, 1).toString(),
                    m.getValueAt(renglonSeleccionado, 2).toString(),
                    m.getValueAt(renglonSeleccionado, 3).toString()
            );
            Actualizar dialogo = new Actualizar(this,true,pe);
            dialogo.setVisible(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(renglonSeleccionado==-1){
            JOptionPane.showMessageDialog(this, "ERROR!!\nSelecciona un renglon para borrar");
            return;
        }
        int respuesta=JOptionPane.showConfirmDialog(this, "Estas seguro de eliminar el campo");
        if(respuesta==JOptionPane.YES_OPTION){
            String  idEliminar = m.getValueAt(renglonSeleccionado, 0).toString();
            if(base.Eliminar(idEliminar)){
                setTitle("SE ELIMINO CORRECTAMENTE");
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(this,"Error!!, no se elimino");
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

