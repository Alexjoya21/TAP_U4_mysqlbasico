    public void cargarTabla(){
        ArrayList<Persona> resultado = base.mostrarTodos();
        
        int tamModelo = m.getRowCount();
        
        for(int i=1;i<tamModelo;i++){
            m.removeRow(0);
        }
        
        resultado.forEach(
                (persona)-> m.addRow(persona.toRenglon())
        );
        jTable1.setModel(m);
    }
